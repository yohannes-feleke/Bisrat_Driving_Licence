    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked

        JTable source = (JTable)evt.getSource();
        int row = source.rowAtPoint( evt.getPoint() );
        int column = source.columnAtPoint( evt.getPoint() );
        String s=source.getModel().getValueAt(row, column)+"";
        try
        {
            //connection
            con = DriverManager.getConnection(DBName,DBUser,DBPass);
            load = "SELECT id FROM result WHERE id=?";
            ps = con.prepareStatement(load);
            ps.setString(1, s);
            rs=ps.executeQuery();
            if(rs.next() && rs.getString("id").equals(s))
            {
                UIManager.put("OptionPane.messageFont", new FontUIResource(new Font(  
                "AbnetZeMelakt", Font.BOLD, 20)));
                int resp = JOptionPane.showConfirmDialog(this, "ለማጥፋት እርግጠኛ ኖት ?","Confirm",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
                if(resp == JOptionPane.YES_OPTION)
                {
                    try
                    {
                        con = DriverManager.getConnection(DBName,DBUser,DBPass);
                        load = "DELETE FROM result WHERE id=?";
                        ps = con.prepareStatement(load);
                        ps.setString(1, s);
                        ps.executeUpdate();

                        UIManager.put("OptionPane.messageFont", new FontUIResource(new Font(  
                        "AbnetZeMelakt", Font.BOLD, 20)));
                        JOptionPane.showMessageDialog(null, "መረጃው ከመረጃ ማህደር ተሰርዟል!!");
                        ShowTable1();    
                    }
                    catch (SQLException ex){   
                        System.out.print(ex);
                    }
                }
                else if(resp == JOptionPane.NO_OPTION)
                {
                    UIManager.put("OptionPane.messageFont", new FontUIResource(new Font(  
                    "AbnetZeMelakt", Font.BOLD, 20)));
                    JOptionPane.showMessageDialog(null, "ተትቷል!");
                }     
            }
        } catch (SQLException ex) {
        System.out.print(ex);
    }
    }//GEN-LAST:event_jTable2MouseClicked

